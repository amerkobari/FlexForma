                <h3>Dumbbell Shrug:</h3>
                Stand with your feet shoulder-width apart, holding a dumbbell in each hand by your sides.<br>
                Allow your arms to hang straight with your palms facing your body.<br>
                Elevate your shoulders as high as possible toward your ears.<br>
                Focus on using your upper trapezius muscles to lift the dumbbells, avoiding excessive movement in other parts of your body.<br>
                Hold the shrug position at the top for a brief moment, contracting your upper traps.<br>
                Lower your shoulders back down in a controlled manner, returning to the starting position.
