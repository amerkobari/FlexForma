                <h3>Lat Pulldown:</h3>
                Sit on a lat pulldown machine with your knees securely positioned under the leg pads.<br>
                Grasp the bar with a wide, overhand grip, hands placed slightly wider than shoulder-width apart.<br>
                Ensure your back is straight, chest up, and shoulders relaxed.<br>
                Pull the bar down towards your chest by engaging your lat muscles.<br>
                Focus on bringing your elbows down and back, squeezing your shoulder blades together at the bottom of the movement.<br>
