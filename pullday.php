                <h3>Bent-Over Row:</h3>
                Stand with your feet shoulder-width apart, holding a barbell or dumbbells in front of you with an overhand grip.<br>
                Hinge at your hips, lowering your torso until it is almost parallel to the ground. Keep your back straight, chest up, and your knees slightly bent.<br>
                Pull the weight(s) toward your lower chest or upper abdomen by retracting your shoulder blades and bending your elbows.<br>
                Keep your elbows close to your body and squeeze your back muscles at the top of the movement.<br>
