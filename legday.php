            <td width="50%">
                <h3>Leg Press:</h3>
                Sit on the leg press machine with your back against the padded backrest.<br>
                Place your feet shoulder-width apart on the footplate, ensuring your knees are aligned with your toes.<br>
                Adjust the seat so that your knees are at a 90-degree angle.<br>
                Unrack the weight by disengaging the safety mechanism.<br>
                Lower the footplate by bending your knees until they form a 90-degree angle.<br>
                Keep your back flat against the seat throughout the movement.<br>
                Push the footplate away from you by extending your knees, straightening your legs.<br>
                Engage your quadriceps and glutes as you press the weight.<br>
                Bend your knees again to lower the footplate, returning to the starting position.<br>
                Control the descent and avoid locking out your knees.
