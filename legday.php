                <h3>Lunge:</h3>
                Stand with your feet together, maintaining a straight posture.<br>
                Take a step forward with one foot, ensuring your knee is directly above your ankle.<br>
                Lower your body toward the ground by bending both knees.<br>
                The back knee should hover just above the floor, forming two 90-degree angles with your front and back legs.<br>
                Push through the heel of your front foot to return to the starting position.<br>
                Keep your core engaged and chest up throughout the movement.<br>
                Repeat the lunge on the opposite leg by taking a step forward with the other foot.<br>
                Continue to alternate between legs for the desired number of repetitions.
