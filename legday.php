                <h3>Pistol Squat:</h3>
                    Stand on one leg, keeping your foot flat on the floor.<br>
                    Extend the other leg straight in front of you, parallel to the ground.<br>
                    Keep your chest up, shoulders back, and core engaged for balance.<br>
                    Initiate the squat by bending your standing knee, lowering your body toward the ground.<br>
                    Keep your back straight and chest up as you descend.<br>
                    Lower your body as far as your mobility allows, ideally reaching a position where your thigh is parallel to the ground.<br>
                    Maintain balance and control throughout the movement.<br>
